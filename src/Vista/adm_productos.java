    public void agregar_producto(Producto pr) {

        pnl_detalle.setVisible(true);

        lbl_codigo.setText(pr.getId().toString());
        txt_codigo_barra.setText(pr.getCodBarra());
        txt_nombre.setText(pr.getDescripcion());
        txt_precio_c.setText(pr.getPrecioCompra().toString());
        txt_precio_v.setText(pr.getPrecioVenta().toString());

        Integer categoria = pr.getCategoriaId();

        int size = cbo_categoria_hide.getItemCount();
        for (int i = 0; i < size; i++) {
            String item = cbo_categoria_hide.getItemAt(i);
            String[] parts = item.split(":");
            int id = Integer.parseInt(parts[0]);
            if (id == categoria) {
                cbo_categoria_hide.setSelectedIndex(i);
                cbo_categoria.setSelectedIndex(i);
                i = size;
            }
        }
    }

