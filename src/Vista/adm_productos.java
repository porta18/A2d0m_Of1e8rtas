    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed

        if (validarOferta()) {

            Integer tienda = 0;
            String tipo = "";
            Integer prd_id = 0;
            Integer codigoBarra = 0;
            String nombre = "";
            Integer categoria = 0;
            Integer precioV = 0;
            Integer precioC = 0;

            String[] partsTienda = cmb_tienda_hide.getSelectedItem().toString().split(":");
            tienda = Integer.parseInt(partsTienda[0]);

            String[] partsCategoria = cbo_categoria_hide.getSelectedItem().toString().split(":");
            categoria = Integer.parseInt(partsCategoria[0]);
            codigoBarra = Integer.parseInt(txt_codigo_barra.getText().toString().trim());
            nombre = txt_nombre.getText().toString().trim();
            precioV = Integer.parseInt(txt_precio_v.getText().toString().trim());
            precioC = Integer.parseInt(txt_precio_c.getText().toString().trim());

            WsProducto ws = new WsProducto();
            String retorno;
            if (lbl_codigo.getText().equals("...")) {
                tipo = "do";
                retorno = ws.WSfn_GuardarProducto(tipo, codigoBarra, nombre, precioC, categoria, precioV, tienda, prd_id);
                MensajeGuardar(retorno);
            } else {
                tipo = "set";
                prd_id = Integer.parseInt(lbl_codigo.getText());
                retorno = ws.WSfn_GuardarProducto(tipo, codigoBarra, nombre, precioC, categoria, precioV, tienda, prd_id);
                MensajeActualiza(retorno);

            }

        }
    }//GEN-LAST:event_btn_guardarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        if (!lbl_codigo.getText().equals("...")) {

            Integer respuesta = JOptionPane.showConfirmDialog(this, "¿Esta Seguro que desea Eliminar?", "Productos", JOptionPane.YES_NO_OPTION);
            if (respuesta == JOptionPane.YES_OPTION) {

                int id_prd = Integer.parseInt(lbl_codigo.getText().toString());
                WsProducto ws = new WsProducto();
                String retorno;
                retorno = ws.WSfn_EliminarProducto(id_prd);
                JOptionPane.showMessageDialog(this, retorno);
                limpiar_form();

            }

        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

    public void MensajeGuardar(String respuesta) {

        String mensaje = "";
        if (!respuesta.equals("")) {
            try {
                Integer.parseInt(respuesta);
                mensaje = "Producto registrado Correctamente";
                limpiar_form();
            } catch (Exception ex) {
                mensaje = "Error WS: " + respuesta;
            }

        } else {
            mensaje = "Error de comunicacion, no se pudo guardar";

        }
        JOptionPane.showMessageDialog(this, mensaje);
    }

    public void MensajeActualiza(String respuesta) {

        String mensaje = "";
        if (!respuesta.equals("")) {

            mensaje = respuesta;
            limpiar_form();

        } else {
            mensaje = "Error de comunicacion, no se pudo actualizar";

        }
        JOptionPane.showMessageDialog(this, mensaje);
    }

    public boolean validarOferta() {

        String mensaje = "";
        boolean valida = true;

        if (cmb_tienda.getSelectedIndex() == -1) {
            mensaje = "Seleccione una tienda";
            valida = false;
        }
        if (valida == true & txt_codigo_barra.getText().toString().trim().equals("")) {
            mensaje = "Ingrese Codigo de barra";
            valida = false;
        } else {
            try {
                Integer.parseInt(txt_codigo_barra.getText());
            } catch (Exception ex) {
                mensaje = "Campo Codigo Barra solo numerico";
                valida = false;
            }
        }
        if (valida == true & txt_nombre.getText().toString().trim().equals("")) {
            mensaje = "ingrese descripcion del producto";
            valida = false;
        }
        if (valida == true & cbo_categoria.getSelectedIndex() == -1) {
            mensaje = "Seleccione una categoria";
            valida = false;
        }
        if (valida == true & txt_precio_c.getText().toString().trim().equals("")) {
            mensaje = "ingrese precio de compra";
            valida = false;
        } else {
            try {
                Integer.parseInt(txt_precio_c.getText().replace(".", ""));
            } catch (Exception ex) {
                mensaje = "Campo Precio Compra solo numerico";
                valida = false;
            }
        }
        if (valida == true & txt_precio_v.getText().toString().trim().equals("")) {
            mensaje = "ingrese precio de venta";
            valida = false;
        } else {
            try {
                Integer.parseInt(txt_precio_v.getText().replace(".", ""));
            } catch (Exception ex) {
                mensaje = "Campo Precio venta solo numerico";
                valida = false;
            }
        }

        if (valida == false) {
            JOptionPane.showMessageDialog(this, mensaje);
        }

        return valida;
    }

